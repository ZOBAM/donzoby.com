   <p>Web design as a discipline is so broad that it has other departments under it in some large corporations. It broadly describes all the planning, programming and graphics works needed to build all the web pages that make up a complete and functional website.</p>
		<p>This term web design most times refers to the work done on the client side (web browser) using markup language, style sheet and scripting language to build a website.</p> 
		<p>Our web design course here will be focusing on the above mentioned aspects of web design.</p> 
